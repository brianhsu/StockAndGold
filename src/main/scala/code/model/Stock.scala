  def updatePrice(stockCode: String) = {

    val infoURL = s"http://www.google.com/finance/info?infotype=infoquoteall&q=TPE:$stockCode"
    val contentFuture = DataGetter(infoURL)

    def updatePriceInDB(responseContent: String) = Try {
      val JArray(List(jsonData)) = parse(responseContent.drop(3))
      val JString(currentPrice) = jsonData \\ "l"
      val JString(minPrice) = jsonData \\ "lo"
      val JString(maxPrice) = jsonData \\ "hi"
      val JString(closePrice) = jsonData \\ "pcls_fix"
      val JString(dateString) = jsonData \\ "lt_dts"
      val dateFormatter = new SimpleDateFormat("yyyy-MM-dd'T'HH:mm:ss")
      val calendar = Calendar.getInstance
      calendar.setTime(dateFormatter.parse(dateString))

      Stock.delete("code", stockCode)
      Stock.createRecord
           .code(stockCode)
           .currentPrice(BigDecimal(currentPrice))
           .minPrice(BigDecimal(minPrice))
           .maxPrice(BigDecimal(maxPrice))
           .closePrice(BigDecimal(closePrice))
           .priceUpdateAt(calendar)
           .saveTheRecord()
    }

    contentFuture.foreach(updatePriceInDB)
  }

